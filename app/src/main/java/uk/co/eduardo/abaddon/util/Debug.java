package uk.co.eduardo.abaddon.util;

/**
 * Debug Constants
 * 
 * @author Eduardo Rodrigues
 */
public class Debug
{
   //================|  Fields             |====================================
   
   /** Draw a dot at the hero position and hide the tile he is stepping on */
   public static boolean showHeroTile = false;
   
   /** Hide the hero sprite */
   public static boolean hideHero = false;
   
   /** Draw the map in wireframe mode */
   public static boolean wireframe = false;
   
   /** Enable the day/night transitions */
   public static boolean useDayNight = false;
   
   /** Show the FPS window */
   public static boolean showFps = true;
   
   /** Show the accelerometer window */
   public static boolean showAccel = false;
   
   /** Show touch information. */
   public static boolean showTouch = false;
   
   /** Turn clipping on and off */
   public static boolean clipOff = false;

   /** Display a path found by the path finding. */
   public static boolean showPath = true;
   
   /** Whether or not accelerometer movement is enabled. */
   public static boolean useAccelerometer = false;
}
